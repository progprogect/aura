/**
 * Утилита для генерации slug из текста
 */

// Таблица транслитерации (кириллица → латиница)
const translitMap: Record<string, string> = {
  'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd',
  'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
  'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
  'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
  'у': 'u', 'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch',
  'ш': 'sh', 'щ': 'sch', 'ъ': '', 'ы': 'y', 'ь': '',
  'э': 'e', 'ю': 'yu', 'я': 'ya',
  'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D',
  'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I',
  'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N',
  'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T',
  'У': 'U', 'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch',
  'Ш': 'Sh', 'Щ': 'Sch', 'Ъ': '', 'Ы': 'Y', 'Ь': '',
  'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
}

/**
 * Транслитерация текста (кириллица → латиница)
 */
function translit(text: string): string {
  return text
    .split('')
    .map(char => translitMap[char] || char)
    .join('')
}

/**
 * Генерирует slug из текста
 * 
 * @example
 * generateSlug('Александр Морозов') // 'alexandr-morozov'
 * generateSlug('Иван Петров-Водкин') // 'ivan-petrov-vodkin'
 */
export function generateSlug(text: string): string {
  return translit(text)
    .toLowerCase()
    .trim()
    // Заменяем пробелы и подчеркивания на дефисы
    .replace(/[\s_]+/g, '-')
    // Удаляем все символы кроме букв, цифр и дефисов
    .replace(/[^a-z0-9-]/g, '')
    // Удаляем повторяющиеся дефисы
    .replace(/-+/g, '-')
    // Удаляем дефисы в начале и конце
    .replace(/^-|-$/g, '')
}

